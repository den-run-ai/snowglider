// @ts-check
/**
 * Rock visual-acceptance metrics (rock realism recovery PR 1, issue #385).
 *
 * The #344 lesson: a rock geometry change can pass every invariant test (determinism,
 * bounded radii, NaN-freedom, "some snow exists") and still be a visual regression.
 * This harness gates the *perceptual* properties as CI-stable numbers computed from
 * the raw geometry/colour buffers of the FIXED gallery samples
 * (src/visual/rock-gallery-samples.ts — the same rocks the rendered gallery shows):
 *
 *   - Mass floors:      avgRadius ≥ 0.95·baseline, aabbVolume ≥ 0.90·baseline
 *                       (the hull attempt shrank every rock — never again).
 *   - Collision guard:  maxHorizontalRadius ≤ 1.05·baseline until #348 lands
 *                       (visible mass must stay inside the size-based hazard radius).
 *   - Snow restraint:   snowVertexRatio ≤ baseline+0.12, areaWeightedSnowRatio ≤
 *                       baseline+0.10, largeWhiteFaceRatio ≤ baseline+0.02
 *                       (the whole-facet-white failure mode).
 *   - Stone visibility: sideStoneRatio ≥ max(0.45, baseline−0.10) — clamped to the
 *                       baseline itself where the current geometry is already below
 *                       the 0.45 target, so the gate can only ratchet up.
 *   - Undersides:       undersideLumRatio ≤ baseline+0.05 (never brighter).
 *   - Relief set:       recorded + logged (radialVariance, p90/p10 spread, axis ratio,
 *                       normal-area entropy, silhouette variance) — floors raised in
 *                       PR 3, which must push these UP, not just avoid floors.
 *
 * Determinism prerequisite: createRock's vertex jitter + makeRockColor draw from the
 * GLOBAL Math.random stream (only the scrape pass is seeded), so every build here pins
 * Math.random to a fixed local stream (the buildPinned pattern from
 * rocks-shape-tests.js). Without that the baseline fixture would be noise.
 *
 * Baseline: tests/fixtures/rock-visual-baseline.json, seeded from the geometry this
 * repo currently ships. Regenerate ONLY on an intentional, reviewed visual change:
 *   node --import ./tests/loaders/register-ts-resolve.mjs \
 *     tests/rock-visual-metrics-tests.js --update-baseline
 *
 * Run: npm run test:rocks:metrics
 */

const fs = require('fs');
const path = require('path');

let pass = 0, fail = 0;
function runTest(name, fn) {
  try { fn(); console.log(`✅ PASS: ${name}`); pass++; }
  catch (e) { console.log(`❌ FAIL: ${name}\n   ${e instanceof Error ? e.message : String(e)}`); fail++; }
}
function assert(cond, msg) { if (!cond) throw new Error(msg || 'assertion failed'); }

// Same deterministic PRNG family the module uses privately; this one REPLACES
// Math.random during builds so the global jitter + colour draws repeat too.
function mulberry32(seed) {
  let a = seed >>> 0;
  return () => {
    a = (a + 0x6D2B79F5) | 0;
    let t = Math.imul(a ^ (a >>> 15), 1 | a);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

const BASELINE_PATH = path.join(__dirname, 'fixtures', 'rock-visual-baseline.json');
const UPDATE = process.argv.includes('--update-baseline');

(async () => {
  const { createRock } = await import('../src/mountains/rocks.js');
  const { ROCK_GALLERY_SAMPLES } = await import('../src/visual/rock-gallery-samples.js');
  const { computeRockMetrics } = await import('./lib/rock-metrics.mjs');

  console.log('\n🪨📏 SNOWGLIDER ROCK VISUAL-METRICS HARNESS 🪨📏');
  console.log('=================================================\n');

  const realRandom = Math.random;
  /** Build one gallery sample with the global stream pinned — keyed per sample
   *  (1234 ^ seed) so each sample carries its own jitter + base stone hue like a
   *  live slope does, while staying independent of build order and bit-identical
   *  run-to-run. Must match rock-gallery.ts, which renders these exact rocks. */
  function buildSample(sample) {
    Math.random = mulberry32((1234 ^ sample.seed) >>> 0);
    try {
      return createRock(sample.size, { cliff: sample.kind !== 'boulder', seed: sample.seed });
    } finally {
      Math.random = realRandom;
    }
  }

  /** @type {Record<string, Record<string, number>>} */
  const current = {};
  for (const sample of ROCK_GALLERY_SAMPLES) {
    const rock = buildSample(sample);
    const attrs = {
      position: rock.geometry.attributes.position.array,
      normal: rock.geometry.attributes.normal.array,
      color: rock.geometry.attributes.color.array,
    };
    current[sample.id] = computeRockMetrics(attrs, sample.size);
  }

  if (UPDATE) {
    fs.mkdirSync(path.dirname(BASELINE_PATH), { recursive: true });
    fs.writeFileSync(BASELINE_PATH, JSON.stringify({
      _comment: 'Rock visual-acceptance baseline (issue #385 PR 1). Generated by ' +
        'tests/rock-visual-metrics-tests.js --update-baseline with Math.random pinned to ' +
        'mulberry32(1234) per build. Regenerate ONLY on an intentional, reviewed visual change.',
      samples: current,
    }, null, 2) + '\n');
    console.log(`Baseline written: ${path.relative(process.cwd(), BASELINE_PATH)} ` +
      `(${ROCK_GALLERY_SAMPLES.length} samples)`);
  }

  runTest('baseline fixture exists and covers exactly the gallery samples', () => {
    assert(fs.existsSync(BASELINE_PATH),
      'missing tests/fixtures/rock-visual-baseline.json — run with --update-baseline once');
    const fixture = JSON.parse(fs.readFileSync(BASELINE_PATH, 'utf8'));
    const baseIds = Object.keys(fixture.samples).sort();
    const liveIds = ROCK_GALLERY_SAMPLES.map((s) => s.id).sort();
    assert(JSON.stringify(baseIds) === JSON.stringify(liveIds),
      `fixture samples [${baseIds}] do not match gallery samples [${liveIds}] — ` +
      'regenerate the baseline deliberately');
  });

  runTest('every metric is finite on every sample (no NaN/Infinity poisoning)', () => {
    for (const [id, metrics] of Object.entries(current)) {
      for (const [name, value] of Object.entries(metrics)) {
        assert(Number.isFinite(value), `${id}.${name} is ${value}`);
      }
    }
  });

  runTest('metrics are deterministic (second pinned build is bit-identical)', () => {
    const sample = ROCK_GALLERY_SAMPLES[0];
    const rock = buildSample(sample);
    const again = computeRockMetrics({
      position: rock.geometry.attributes.position.array,
      normal: rock.geometry.attributes.normal.array,
      color: rock.geometry.attributes.color.array,
    }, sample.size);
    for (const [name, value] of Object.entries(current[sample.id])) {
      assert(again[name] === value, `${sample.id}.${name} drifted: ${again[name]} vs ${value}`);
    }
  });

  const fixture = fs.existsSync(BASELINE_PATH)
    ? JSON.parse(fs.readFileSync(BASELINE_PATH, 'utf8'))
    : null;

  for (const sample of ROCK_GALLERY_SAMPLES) {
    runTest(`${sample.id}: mass, snow-restraint, and stone-visibility thresholds`, () => {
      assert(fixture, 'no baseline fixture');
      const base = fixture.samples[sample.id];
      assert(base, `no baseline entry for ${sample.id}`);
      const m = current[sample.id];
      const EPS = 1e-9;

      // Mass floors (#344 shrinkage guard)
      assert(m.avgRadius >= 0.95 * base.avgRadius - EPS,
        `avgRadius ${m.avgRadius.toFixed(4)} < 0.95·baseline (${base.avgRadius.toFixed(4)})`);
      assert(m.aabbVolume >= 0.90 * base.aabbVolume - EPS,
        `aabbVolume ${m.aabbVolume.toFixed(4)} < 0.90·baseline (${base.aabbVolume.toFixed(4)})`);

      // Horizontal collision guard (held until #348 / PR 5)
      assert(m.maxHorizontalRadius <= 1.05 * base.maxHorizontalRadius + EPS,
        `maxHorizontalRadius ${m.maxHorizontalRadius.toFixed(4)} > 1.05·baseline ` +
        `(${base.maxHorizontalRadius.toFixed(4)}) — visible mass would outgrow the ` +
        'size-based hazard radius before height-aware collision lands');

      // Snow restraint caps (#344 over-white guard)
      assert(m.snowVertexRatio <= base.snowVertexRatio + 0.12 + EPS,
        `snowVertexRatio ${m.snowVertexRatio.toFixed(4)} > baseline+0.12 (${base.snowVertexRatio.toFixed(4)})`);
      assert(m.areaWeightedSnowRatio <= base.areaWeightedSnowRatio + 0.10 + EPS,
        `areaWeightedSnowRatio ${m.areaWeightedSnowRatio.toFixed(4)} > baseline+0.10 (${base.areaWeightedSnowRatio.toFixed(4)})`);
      assert(m.largeWhiteFaceRatio <= base.largeWhiteFaceRatio + 0.02 + EPS,
        `largeWhiteFaceRatio ${m.largeWhiteFaceRatio.toFixed(4)} > baseline+0.02 ` +
        `(${base.largeWhiteFaceRatio.toFixed(4)}) — large fully-white facets are the #344 failure mode`);

      // Stone visibility floor: target 0.45, but never demand more than the current
      // baseline already delivers (the gate ratchets up via baseline regen, PR 2).
      const sideFloor = Math.min(Math.max(0.45, base.sideStoneRatio - 0.10), base.sideStoneRatio);
      assert(m.sideStoneRatio >= sideFloor - EPS,
        `sideStoneRatio ${m.sideStoneRatio.toFixed(4)} < floor ${sideFloor.toFixed(4)}`);

      // Undersides must never get BRIGHTER relative to the sides.
      assert(m.undersideLumRatio <= base.undersideLumRatio + 0.05 + EPS,
        `undersideLumRatio ${m.undersideLumRatio.toFixed(4)} > baseline+0.05 (${base.undersideLumRatio.toFixed(4)})`);
    });
  }

  // Relief targets: recorded and surfaced, not yet enforced (PR 3 raises floors).
  console.log('\n--- Relief targets (recorded for PR 3; higher = craggier) ---');
  for (const sample of ROCK_GALLERY_SAMPLES) {
    const m = current[sample.id];
    console.log(
      `${sample.id.padEnd(14)} radialVar=${m.radialVariance.toFixed(4)} ` +
      `p90/p10=${m.radiusP90P10.toFixed(3)} axis=${m.axisRatio.toFixed(3)} ` +
      `entropy=${m.normalAreaEntropy.toFixed(3)} silCV=${m.silhouetteAreaCV.toFixed(4)} ` +
      `largestFace=${m.largestFaceAreaRatio.toFixed(3)} snow=${m.areaWeightedSnowRatio.toFixed(3)} ` +
      `sideStone=${m.sideStoneRatio.toFixed(3)}`);
  }

  console.log(`\n==================================`);
  console.log(`Rock visual-metrics tests: ${pass} passed, ${fail} failed`);
  if (fail > 0) process.exit(1);
})().catch((e) => {
  console.error('Rock visual-metrics harness crashed:', e);
  process.exit(1);
});
